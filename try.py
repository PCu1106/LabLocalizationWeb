def LR_to_Table(LR):
    if LR[-1] == 'L':
        LR = LR[:-1]
        row = int(LR[-2:])
        LR = LR[0:-2]
        col = int(LR)*3-2
    elif LR[-1] == 'R':
        LR = LR[:-1]
        row = int(LR[-2:])
        LR = LR[0:-2]
        col = int(LR)*3
    else:
        row = int(LR[-2:])
        LR = LR[0:-2]
        col = int(LR)*3-1
    return [row, col]


